package day1

/*
Arithmetic operator
Relation operator
Assignment operator
Unary operator
Bitwise operation
Logical operator
 */

fun main(args : Array<String>) {
    var a=10;
    var b=5;
    println(a+b);
    println(a.plus(b))
    println(a-b);
    println(a.minus(b))
    println(a*b);
    println(a.times(b))
    println(a/b);
    println(a.div(b))
    println(a%b);
    print(a.rem(b))
}