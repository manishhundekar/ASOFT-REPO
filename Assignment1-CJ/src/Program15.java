
public class Program15 {

	public static void main(String[] args) {
		//Part -A
		for (int i=3 ; i>0 ; i--)
		{
			for (int j=1 ; j<=i ; j++)
			{
				System.out.print("#");
			}
			System.out.print('\n');
		}
		
		//Part-B
		for (int i=0 ; i<4 ; i++)
		{
			for (int j=0 ; j<=i ; j++)
			{
				System.out.print("*");
			}
			System.out.print('\n');
		}
	
	}

}
